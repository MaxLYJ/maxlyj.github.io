#!/usr/bin/env node
// prerender-project-pages.js
//
// Build-time pre-render for the JS-rendered project instance pages
// (farcry-6.html, division-2.html, d-walker-vs-sahelanthropus.html,
// raiden-vs-gekko.html). Each page builds its entire <body> at runtime in
// project-instance-loader.js (fetch template-content.html + a per-project JSON
// config, construct the DOM in JS), so a no-JS fetch sees an empty document and
// a crawler's immediate first pass (before the deferred JS-rendering pass) sees
// only the <head> meta + JSON-LD.
//
// This script inlines each project's REAL content — title, description, every
// project-details block (headings, paragraphs, images, image-comparisons,
// video), and the tools/languages/period/role/tags metadata — as semantic HTML
// inside a <noscript> block in the page <body>.
//
// It ALSO generates each page's schema.org Article JSON-LD <script> block in
// the <head> from the config's `seo` block (headline / description / image /
// keywords) + the canonical URL derived from CNAME. This replaces the four
// hand-maintained JSON-LD blocks (raw JSON typed into HTML, no validity check,
// duplicated @context / @type / author / publisher boilerplate) with a single
// generated, JSON.stringify-validated block that cannot drift from the config.
//
// Why <noscript> and not primary <body> content:
//   - Browsers do not render <noscript> content when JavaScript is enabled, so
//     the inlined content can NEVER duplicate, flash, or conflict with the
//     runtime-rendered page. The loader is untouched and its behavior is
//     unchanged for JS visitors.
//   - No-JS visitors and crawlers that fetch-and-parse without executing JS see
//     real, readable text + images instead of "This page requires JavaScript".
//   - This is the lowest-risk option that needs no browser smoke-test: its only
//     failure mode is "does nothing extra", never "breaks the page".
//
// Idempotent: re-running replaces any previously generated block (delimited by
// the BEGIN/END marker comments) and the legacy bare <noscript> line, then
// re-inserts a fresh block before <script src="shared.js">; it also replaces
// any prior generated JSON-LD block AND the iteration-9 hand-maintained
// JSON-LD block, re-inserting a fresh one before the Google Fonts preconnect.
//
// Usage:   node scripts/prerender-project-pages.js     (from the repo root)
// No dependencies. Node >= 16 (uses fs / path only).

"use strict";

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const CONFIG_DIR = path.join(ROOT, "Resources", "Project Instances", "config");
const TAXONOMY_PATH = path.join(ROOT, "data", "taxonomy.json");
const SHARED_JS_PATH = path.join(ROOT, "shared.js");
const CNAME_PATH = path.join(ROOT, "CNAME");

const BEGIN_MARKER = "<!-- BEGIN project-noscript (generated by scripts/prerender-project-pages.js from Resources/Project Instances/config/*.json + data/taxonomy.json — do not hand-edit; re-run the script after changing a config) -->";
const END_MARKER = "<!-- END project-noscript -->";

// Markers for the generated <head> JSON-LD block (idempotent, same model as the
// noscript markers above). The Google Fonts preconnect link is the stable
// insertion anchor present in every project page's <head>.
const JSONLD_BEGIN = "<!-- BEGIN project-jsonld (generated by scripts/prerender-project-pages.js from the config's `seo` block — do not hand-edit; re-run the script after changing it) -->";
const JSONLD_END = "<!-- END project-jsonld -->";
const PRECONNECT_ANCHOR = '<link rel="preconnect" href="https://fonts.googleapis.com" />';

// The four live project pages and the config slug each carries on <body>.
const PAGES = [
  { html: "farcry-6.html", slug: "farcry6-procedural-generation" },
  { html: "division-2.html", slug: "division2-tools" },
  { html: "d-walker-vs-sahelanthropus.html", slug: "d-walker-vs-sahelanthropus" },
  { html: "raiden-vs-gekko.html", slug: "raiden-vs-gekko" }
];

// Read CDN_BASE from shared.js so the build tool and the runtime share one
// source of truth (the script fails loudly if it cannot find it).
function readCdnBase() {
  const src = fs.readFileSync(SHARED_JS_PATH, "utf8");
  const match = src.match(/const\s+CDN_BASE\s*=\s*"([^"]+)"/);
  if (!match) {
    throw new Error(`Could not find CDN_BASE in ${SHARED_JS_PATH}. Re-run is unsafe.`);
  }
  return match[1];
}

// Mirror of shared.js toCdnUrl: absolute URLs pass through; logical paths are
// prefixed with the R2 base.
function toCdnUrl(cdnBase, value) {
  if (!value) return value;
  if (/^https?:\/\//i.test(value)) return value;
  return cdnBase + "/" + String(value).replace(/^\/+/, "");
}

function escapeHtml(value) {
  return String(value == null ? "" : value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

// Normalize a meta field that may be a string or an array of strings.
function metaText(value) {
  if (Array.isArray(value)) {
    return value.map((part) => String(part).trim()).filter(Boolean).join(", ");
  }
  return String(value == null ? "" : value).trim();
}

// Read the canonical site base (e.g. "https://maxlyj.com") from CNAME so the
// generated JSON-LD url / mainEntityOfPage agree with each page's <link
// rel="canonical">. Falls back to maxlyj.com if CNAME is absent.
function readSiteBase() {
  let host = "maxlyj.com";
  try {
    const raw = fs.readFileSync(CNAME_PATH, "utf8").trim();
    if (raw) host = raw;
  } catch (_e) {
    console.warn(`  ! CNAME not found at ${CNAME_PATH}; falling back to https://${host}.`);
  }
  return "https://" + host;
}

// Prefix every line of `text` with `prefix` (used to indent JSON.stringify
// output so it nests cleanly inside the <head> column).
function indentBlock(text, prefix) {
  return text
    .split("\n")
    .map((line) => prefix + line)
    .join("\n");
}

// Build the schema.org Article JSON-LD object for a project page from its
// config.seo block. headline/description/image/keywords are the hand-curated
// SEO values (they intentionally differ from config.title / config.images.cover
// for some projects — e.g. the social/JSON-LD image is projects/<slug>/cover.*
// while the in-page hero cover may be a different file). `url` is derived from
// the site base + the page filename so it always matches <link rel="canonical">
// without being hand-maintained per page. Author/publisher are the portfolio
// owner (self-published case studies).
function buildJsonLdObject(config, pageFilename, siteBase) {
  const seo = (config && config.seo) || {};
  const canonical = siteBase + "/" + pageFilename;
  const person = {
    "@type": "Person",
    name: "Yujia Max Liu",
    url: siteBase + "/"
  };
  return {
    "@context": "https://schema.org",
    "@type": "Article",
    mainEntityOfPage: { "@type": "WebPage", "@id": canonical },
    headline: seo.headline || config.title || "Project",
    description: seo.description || config.description || "",
    image: seo.image || (config.images && config.images.cover) || "",
    url: canonical,
    author: person,
    publisher: person,
    keywords: seo.keywords || ""
  };
}

// Serialize the JSON-LD object to an indented <script type="application/ld+json">
// block wrapped in the BEGIN/END markers. JSON.stringify guarantees the emitted
// JSON is valid (the prior hand-maintained blocks were raw JSON typed into HTML
// across four files with no validity check).
function buildJsonLdHtml(config, pageFilename, siteBase) {
  const obj = buildJsonLdObject(config, pageFilename, siteBase);
  const json = JSON.stringify(obj, null, 2);
  // Indent the JSON body to nest inside <head>'s 4-space column: the opening
  // brace lands at column 6 and each key at column 8, matching the layout of
  // the prior hand-maintained JSON-LD block.
  const indented = indentBlock(json, "      ");
  return (
    JSONLD_BEGIN + "\n" +
    "    <script type=\"application/ld+json\">\n" +
    indented + "\n" +
    "    </script>\n" +
    "    " + JSONLD_END
  );
}

// Remove a previously generated JSON-LD block (between the BEGIN/END markers,
// inclusive) plus its trailing whitespace. Idempotent on re-runs.
function stripGeneratedJsonLd(html) {
  const re = new RegExp(
    JSONLD_BEGIN.replace(/[.*+?^${}()|[\]\\]/g, "\\$&") +
      "[\\s\\S]*?" +
      JSONLD_END.replace(/[.*+?^${}()|[\]\\]/g, "\\$&") +
      "\\s*",
    "g"
  );
  return html.replace(re, "");
}

// Remove the iteration-9 hand-maintained JSON-LD: its explanation comment and
// any raw <script type="application/ld+json"> block. The generated block is
// marker-delimited and removed by stripGeneratedJsonLd first; this catches the
// legacy hand-written copy so the committed HTML carries only the generated
// block. (Harmless no-op on re-runs once the hand-maintained copy is gone.)
function stripHandMaintainedJsonLd(html) {
  const withoutComment = html.replace(
    /^[ \t]*<!-- JSON-LD structured data:[\s\S]*?-->[ \t]*\r?\n?/m,
    ""
  );
  return withoutComment.replace(
    /^[ \t]*<script type="application\/ld\+json">[\s\S]*?<\/script>[ \t]*\r?\n?/gm,
    ""
  );
}

// Render one project-details block to semantic HTML (mirrors the runtime
// createDetailBlockElement in project-instance-loader.js, minus the interactive
// widgets which have no static equivalent).
function renderBlock(cdnBase, block) {
  const type = String(block.type || "").toLowerCase();
  const text = escapeHtml(block.text || "");

  if (type === "h1" || type === "h2" || type === "h3" || type === "p") {
    return `      <${type}>${text}</${type}>`;
  }

  if (type === "image") {
    if (!block.src) return null;
    const src = escapeHtml(toCdnUrl(cdnBase, block.src));
    const alt = escapeHtml(block.alt || "");
    return `      <figure class="project-noscript__figure"><img src="${src}" alt="${alt}" loading="lazy" decoding="async" />${alt ? `\n        <figcaption>${alt}</figcaption>\n      ` : ""}</figure>`;
  }

  if (type === "image-compare") {
    if (!block.before && !block.after) return null;
    const lines = ["      <figure class=\"project-noscript__figure project-noscript__compare\">"];
    if (block.after) {
      lines.push(`        <img src="${escapeHtml(toCdnUrl(cdnBase, block.after))}" alt="${escapeHtml(block.afterAlt || "After")}" loading="lazy" decoding="async" />`);
    }
    if (block.before) {
      lines.push(`        <img src="${escapeHtml(toCdnUrl(cdnBase, block.before))}" alt="${escapeHtml(block.beforeAlt || "Before")}" loading="lazy" decoding="async" />`);
    }
    const caption = [block.beforeLabel || "Before", block.afterLabel || "After"]
      .filter(Boolean)
      .join(" / ");
    lines.push(`        <figcaption>${escapeHtml(caption)} comparison</figcaption>`);
    lines.push("      </figure>");
    return lines.join("\n");
  }

  if (type === "video") {
    if (!block.url) return null;
    const url = escapeHtml(block.url);
    const label = escapeHtml(block.title || "Project video");
    return `      <p class="project-noscript__video">Video: <a href="${url}">${label}</a></p>`;
  }

  return null;
}

function buildTagLabels(taxonomy, slug) {
  const tagLabelById = new Map(
    (taxonomy.tags || [])
      .filter((tag) => tag && typeof tag.id === "string")
      .map((tag) => [tag.id, tag.label || tag.id])
  );
  const entry = (taxonomy.projects || []).find((project) => project && project.slug === slug);
  if (!entry || !Array.isArray(entry.tagIds)) return [];
  return entry.tagIds
    .map((id) => tagLabelById.get(id))
    .filter((label) => Boolean(label) && label !== "All");
}

function buildNoscriptHtml(cdnBase, config, taxonomy, slug) {
  const title = escapeHtml(config.title || "Project");
  const kicker = escapeHtml(config.kicker || "");
  const description = escapeHtml(config.description || "");
  // The loader assigns the cover VERBATIM (coverImage.src = config.images.cover
  // — no toCdnUrl), so three covers are full R2 URLs and raiden's is a local
  // repo path. Mirror that exactly: using toCdnUrl here would wrongly prefix
  // raiden's local path and produce a non-existent R2 URL.
  const cover = config.images && config.images.cover ? config.images.cover : "";
  const tags = buildTagLabels(taxonomy, slug).map(escapeHtml);

  const metaRows = [
    ["Tools", metaText(config.tools)],
    ["Languages", metaText(config.languages)],
    ["Period", metaText(config.time)],
    ["Role", metaText(config.role)]
  ].filter(([, value]) => value.length > 0);

  const blocks = Array.isArray(config.projectDetails && config.projectDetails.blocks)
    ? config.projectDetails.blocks
    : [];

  const lines = [];
  lines.push("  <noscript>");
  lines.push(`    <article class="project-noscript">`);
  // When JS is off the loader also does not inject the top bar / sidebar, so
  // there is no site navigation at all — provide a back-to-home link (also a
  // useful internal link for non-JS crawlers).
  lines.push(`      <p class="project-noscript__home"><a href="/">&larr; Back to all projects</a></p>`);
  if (kicker) {
    lines.push(`      <p class="project-noscript__kicker">${kicker}</p>`);
  }
  lines.push(`      <h1>${title}</h1>`);
  if (description) {
    lines.push(`      <p class="project-noscript__description">${description}</p>`);
  }
  if (cover) {
    lines.push(`      <img class="project-noscript__cover" src="${escapeHtml(cover)}" alt="${title} cover image" loading="lazy" decoding="async" />`);
  }
  if (metaRows.length > 0 || tags.length > 0) {
    lines.push(`      <dl class="project-noscript__meta">`);
    metaRows.forEach(([label, value]) => {
      lines.push(`        <dt>${escapeHtml(label)}</dt>`);
      lines.push(`        <dd>${escapeHtml(value)}</dd>`);
    });
    if (tags.length > 0) {
      lines.push(`        <dt>Tags</dt>`);
      lines.push(`        <dd>${tags.join(" · ")}</dd>`);
    }
    lines.push(`      </dl>`);
  }
  if (blocks.length > 0) {
    lines.push(`      <section class="project-noscript__details">`);
    const rendered = blocks
      .map((block) => renderBlock(cdnBase, block))
      .filter((line) => line !== null);
    lines.push(...rendered);
    lines.push(`      </section>`);
  }
  lines.push(`      <p class="project-noscript__note">This case study is shown above as text and images. The full interactive version — gallery, image-compare slider, and related works — requires JavaScript.</p>`);
  lines.push(`    </article>`);
  lines.push("  </noscript>");

  return `${BEGIN_MARKER}\n${lines.join("\n")}\n  ${END_MARKER}`;
}

// Remove a previously generated block (between BEGIN/END markers, inclusive).
function stripGeneratedBlock(html) {
  const re = new RegExp(
    BEGIN_MARKER.replace(/[.*+?^${}()|[\]\\]/g, "\\$&") +
      "[\\s\\S]*?" +
      END_MARKER.replace(/[.*+?^${}()|[\]\\]/g, "\\$&") +
      "\\s*",
    "g"
  );
  return html.replace(re, "");
}

// Remove the legacy bare <noscript> line carried since iteration 1.
function stripLegacyNoscript(html) {
  return html.replace(
    /\s*<noscript>\s*This page requires JavaScript to load the shared project template\.\s*<\/noscript>/,
    ""
  );
}

function processPage(page, cdnBase, siteBase, taxonomy) {
  const htmlPath = path.join(ROOT, page.html);
  const configPath = path.join(CONFIG_DIR, `${page.slug}.json`);

  const config = JSON.parse(fs.readFileSync(configPath, "utf8"));
  let html = fs.readFileSync(htmlPath, "utf8");

  // --- <noscript> body fallback (semantic HTML for no-JS visitors / crawlers) ---
  // Strip any prior generation + the legacy bare noscript so re-runs are clean.
  html = stripGeneratedBlock(html);
  html = stripLegacyNoscript(html);

  const blockHtml = buildNoscriptHtml(cdnBase, config, taxonomy, page.slug);

  const scriptMarker = '<script src="shared.js"></script>';
  const idx = html.indexOf(scriptMarker);
  if (idx === -1) {
    throw new Error(`Could not find "${scriptMarker}" in ${page.html}; aborting.`);
  }

  // Insert the generated block immediately before the shared.js script tag,
  // preserving the page's existing indentation (4 spaces).
  html =
    html.slice(0, idx) +
    blockHtml + "\n    " +
    html.slice(idx);

  // --- <head> schema.org Article JSON-LD (generated from the config seo block) ---
  // Strip any prior generated block + the iteration-9 hand-maintained JSON-LD
  // (its explanation comment and raw ld+json script) so only the generated
  // block remains and re-runs are clean.
  html = stripGeneratedJsonLd(html);
  html = stripHandMaintainedJsonLd(html);

  const jsonLdHtml = buildJsonLdHtml(config, page.html, siteBase);
  const preIdx = html.indexOf(PRECONNECT_ANCHOR);
  if (preIdx === -1) {
    throw new Error(`Could not find the Google Fonts preconnect anchor in ${page.html}; aborting.`);
  }
  // Insert the generated JSON-LD immediately before the preconnect link,
  // preserving the page's existing indentation (4 spaces).
  html = html.slice(0, preIdx) + jsonLdHtml + "\n    " + html.slice(preIdx);

  fs.writeFileSync(htmlPath, html, "utf8");
  console.log(`  ✓ ${page.html}  (slug: ${page.slug})`);
}

function main() {
  const cdnBase = readCdnBase();
  const siteBase = readSiteBase();
  const taxonomy = JSON.parse(fs.readFileSync(TAXONOMY_PATH, "utf8"));

  console.log("Pre-rendering project pages (noscript fallback + JSON-LD)...");
  console.log(`  CDN_BASE  = ${cdnBase}`);
  console.log(`  SITE_BASE = ${siteBase}`);
  for (const page of PAGES) {
    processPage(page, cdnBase, siteBase, taxonomy);
  }
  console.log("Done. Commit the updated *.html files.");
}

main();
